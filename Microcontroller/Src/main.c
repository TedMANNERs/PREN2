/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t ADCbuffer[ADC_CH_AMOUNT];	// 0: IMU X-Axis
										// 1: IMU Y-Axis
										// 2: IMU Z-Axis
										// 3: DistanceSensor straight
										// 4: DistanceSensor tilted

	uint16_t MainCounter  = 0;			// Main Counter, increases every 10 ms

	int8_t IMU_OffsetCalibration[3];

	bool     Buzzer_ActiveFlag   = false;
	uint16_t Buzzer_ResetCounter = 0;

	int16_t Encoder_Speed[2]             = {0, 0};
	int16_t Motor_TargetSpeedReceived[2] = {0, 0};  // Received target speed value
	int16_t Motor_TargetSpeed[2]         = {0, 0};  // Real target speed value, which is used as input for the motor control (this is the smoothed version of Motor_TargetSpeedReceived)

	bool JetsonReady = false;

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  MX_USB_DEVICE_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  /* IMU Offset-Calibration on startup */
  HAL_ADC_Start_DMA(&hadc1, &ADCbuffer[0], ADC_CH_AMOUNT);  // Start first ADC Conversion for IMU Offset-Calibration
  HAL_Delay(100);  // Delay in ms  // wait for first conversion to complete

  IMU_calibrate_offset(IMU_OffsetCalibration, ADCbuffer);

  /* Initialize Timers */
  TIM_Init(htim2, htim3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

	  if((Buzzer_ActiveFlag == 1) && (MainCounter >= Buzzer_ResetCounter))
	  {
		  buzzer_off();
		  Buzzer_ActiveFlag = 0;
	  }

	  if(startButton_raisingEdge())
	  {
		  if(JetsonReady)
		  {
			  // notify Navigation-Computer that start command has been issued by the user
			  USB_send_start();

			  // notify user by a short beep
			  buzzer_initiate_shortbeep(&Buzzer_ActiveFlag, MainCounter, &Buzzer_ResetCounter);
		  }
		  else
		  {
			  // Jetson is not ready yet -> notify user by a long beep
			  buzzer_initiate_longbeep(&Buzzer_ActiveFlag, MainCounter, &Buzzer_ResetCounter);
		  }
	  }

	  if(imu_calibrationButton_pressed())
	  {
		  IMU_calibrate_offset(IMU_OffsetCalibration, ADCbuffer);
	  }

	  if(USBreceivedFlag)
	  {
		  USBreceivedFlag = 0;
		  USB_receive_handle(Motor_TargetSpeedReceived, MainCounter, &Buzzer_ActiveFlag, &Buzzer_ResetCounter, &JetsonReady);
	  }

	  if((MainCounter % 10) == 0) // every 10th time (every 100 ms)
	  {
		  Encoder_calculateSpeed(Encoder_Speed);

		  if(JetsonReady)
		  {
			  USB_send_sensordata(ADCbuffer, IMU_OffsetCalibration, Encoder_Speed);
		  }

		  adjust_TargetSpeed(Motor_TargetSpeed, Motor_TargetSpeedReceived);

		  Motor_Control(htim3, Encoder_Speed, Motor_TargetSpeed);

		  /* Start new ADC-Conversions */
		  HAL_ADC_Start_DMA(&hadc1, ADCbuffer, ADC_CH_AMOUNT);
	  }

	  ++MainCounter;
	  HAL_Delay(10);  // Delay in ms
  }

  /* USER CODE END 3 */

}

